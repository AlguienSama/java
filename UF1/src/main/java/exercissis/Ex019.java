public class Ex019 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int cptd = 12;
        System.out.println("counter is: " + cptd); // 12
        System.out.println("counter is: " + ++cptd); // 13
        System.out.println("counter is: " + cptd); // 13
        System.out.println("counter is: " + cptd--); // 13
        System.out.println("counter is: " + cptd); // 12
        System.out.println("counter is: " + -cptd); // -12
    }
    
}
